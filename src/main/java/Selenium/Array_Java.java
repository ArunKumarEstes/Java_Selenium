package Selenium;

public class Array_Java {

	public static void main(String[] args) {
		int[] runner = { 1, 2, 3, 4, 5 };

		for (int i = runner.length - 1; i >= 0; i--) {
			System.out.println(runner[i]);
		}

		for (int i = 1; i <= runner.length; i++) {
			System.out.println(i);

		}

	}
}
